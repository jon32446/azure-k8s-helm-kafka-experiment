#============================================================================
# Create the Azure container registry
#============================================================================

#----------------------------------------------------------------------------
# Config
#----------------------------------------------------------------------------
REGION_NAME=westeurope
RESOURCE_GROUP=ppap-rg
ACR_NAME=ppapcr

#----------------------------------------------------------------------------
# Container registry
#----------------------------------------------------------------------------
az acr create \
    --resource-group $RESOURCE_GROUP \
    --name $ACR_NAME \
    --sku Basic
